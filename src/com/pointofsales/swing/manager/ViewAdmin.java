    private void getSelectedItem() {
        try {
            String sql = "Select * From Admin where AdminID = ?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, (String) jTable1.getValueAt(jTable1.getSelectedRow(), 0));
            rs = pst.executeQuery();
            if (rs.next()) {
                txtNL.setText(rs.getString(2));
                txtAlamat.setText(rs.getString(6));
                txtEMail.setText(rs.getString(5));
                txtNoTelp.setText(rs.getString(4));
                cmbJKelamin.setSelectedItem((String) jTable1.getValueAt(jTable1.getSelectedRow(), 2));
                cmbRole.setSelectedItem((String) jTable1.getValueAt(jTable1.getSelectedRow(), 6));
                getGenderID();
                getRoleID();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Terjadi Error Pada: \n" + ex.toString(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

