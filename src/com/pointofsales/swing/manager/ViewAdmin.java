    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTable1.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Data Tidak Dipilih");
        } else {
            int questions = JOptionPane.showConfirmDialog(null, (String) jTable1.getValueAt(jTable1.getSelectedRow(), 2) + " Ini Akan Dihapus..?", "Perhatian", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (questions == JOptionPane.YES_OPTION) {
                cp.delItem((String) jTable1.getValueAt(jTable1.getSelectedRow(), 0));
                initForms();
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus...");
                tableRenderer();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTable1.getSelectedRow() == 0) {
            JOptionPane.showMessageDialog(this, "Data Harus Dipilih Dulu.");
        } else if (txtNL.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama Lengkap Harus Diisi");
            txtNL.requestFocus();
        } else if (cmbJKelamin.getSelectedItem().equals("<Pilih Data>")) {
            JOptionPane.showMessageDialog(this, "Jenis Kelamin Harus Diisi");
            cmbJKelamin.requestFocus();
        } else if (txtNoTelp.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No. Telepon Harus Diisi");
            txtNoTelp.requestFocus();
        } else if (txtAlamat.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Alamat Harus Harus Diisi");
            txtAlamat.requestFocus();
        } else if (cmbRole.getSelectedItem() == "<Pilih Data>") {
            JOptionPane.showMessageDialog(this, "Satuan Barang Harus Diisi");
            cmbRole.requestFocus();
        } else {
            String AdminID = (String) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
            String FullName = txtNL.getText();
            String Phone = txtNoTelp.getText();
            String EMail = txtEMail.getText();
            String Address = txtAlamat.getText();
//            String Pass = jPasswordField1.getText();
//            Date td = new Date();
//            java.sql.Timestamp sqlTime = new java.sql.Timestamp(td.getTime());
            cp.editItem(AdminID, FullName, getGenderID(), Phone, EMail, Address, getRoleID());
            JOptionPane.showMessageDialog(null, "Data Telah Disimpan");
            initForms();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (txtNL.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama Lengkap Harus Diisi");
            txtNL.requestFocus();
        } else if (cmbJKelamin.getSelectedItem().equals("<Pilih Data>")) {
            JOptionPane.showMessageDialog(this, "Jenis Kelamin Harus Diisi");
            cmbJKelamin.requestFocus();
        } else if (txtNoTelp.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No. Telepon Harus Diisi");
            txtNoTelp.requestFocus();
        } else if (txtAlamat.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Alamat Harus Harus Diisi");
            txtAlamat.requestFocus();
        } else if (cmbRole.getSelectedItem() == "<Pilih Data>") {
            JOptionPane.showMessageDialog(this, "Satuan Barang Harus Diisi");
            cmbRole.requestFocus();
        } else {
            String AdminID = null;
            String FullName = txtNL.getText();
            String Phone = txtNoTelp.getText();
            String EMail = txtEMail.getText();
            String Address = txtAlamat.getText();
            String Pass = jPasswordField1.getText();
            Date td = new Date();
            java.sql.Timestamp sqlTime = new java.sql.Timestamp(td.getTime());
            cp.addItem(AdminID, FullName, getGenderID(), Phone, EMail, Address, sqlTime, getRoleID(), Pass);
            JOptionPane.showMessageDialog(null, "Data Telah Disimpan");
            initForms();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

