    private String getGenderID() {
        String genderID = null;
        try {
            String sql = "SELECT * FROM Gender WHERE GenderName = ?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, (String) cmbJKelamin.getSelectedItem());
            rs = pst.executeQuery();
            if (rs.next()) {
                genderID = rs.getString(1);
                System.out.println(genderID);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Terjadi Error Pada: \n" + ex.toString(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        return genderID;
    }

