    private int findAdminByName() {
        int adminID = 0;
        try {
            String sql = "SELECT * FROM Admin WHERE FullName = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, jTextField1.getText());
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                adminID = rs.getInt(1);
                jPasswordField1.setText(rs.getString(9));
                System.out.println(adminID = Integer.parseInt(rs.getString(1)));
            }
        } catch (SQLException ex) {
            dial.showErrorMessage("Terjadi Error Pada:\n" + ex.toString());
        }
        return adminID;
    }

