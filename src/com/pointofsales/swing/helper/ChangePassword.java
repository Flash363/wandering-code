    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (findAdminByName() == 0) {
            JOptionPane.showMessageDialog(null, "ID. Admin Tidak Ada.\nSilakan Masukan Nama Admin Dahulu...");
            jTextField1.requestFocus();
        } else {
            int res = JOptionPane.showConfirmDialog(null, "Anda Yakin Akan Menyimpan Perubahan Ini...?", "Informasi", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (res == JOptionPane.YES_OPTION) {
                cs.updatePasswordAdmin(findAdminByName(), jPasswordField3.getText());
                dial.showInformationMessage("Perubahan Berhasil Disimpan");
                open();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

