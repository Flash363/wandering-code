    private void authLogin() {
        count += 1;
        java.util.Date tgl = new Date();
        kal = new SimpleDateFormat("EEEE, d MMMM, y");
        time = new SimpleDateFormat("HH:mm:ss z");
        if (count > 4) {
            JOptionPane.showMessageDialog(this, "Anda Telah Gagal Login Sebanyak " + count + " Kali.\n"
                    + "Sistem Kami Mengira Anda Akan Meretas Sistem Kami.\n"
                    + "Sistem Akan Dihancurkan", "Ancaman Ditemukan..!!!", JOptionPane.ERROR_MESSAGE, iconBroken);
            this.setVisible(false);
            System.exit(0);
        } else {
            if (jTextField1.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Silakan Isi Nama Admin Anda ...!", "Error", JOptionPane.ERROR_MESSAGE, icon);
                jTextField1.requestFocus();
            } else if (jPasswordField1.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Silakan Isi Password Anda ...!", "Error", JOptionPane.ERROR_MESSAGE, icon);
                jPasswordField1.requestFocus();
            } else {
                try {
                    String sql = "SELECT * FROM `Admin` WHERE `FullName` = ? AND `Password` = ?";
                    pst = conn.prepareStatement(sql);
                    pst.setString(1, jTextField1.getText());
                    pst.setString(2, jPasswordField1.getText());
                    rs = pst.executeQuery();
                    if (rs.next()) {
                        authUserPrivillege();
                        mainMenu.jLabel2.setText("Admin : " + rs.getString(2));
                        mainMenu.jLabel5.setText("Tanggal : " + time.format(date));
                        mainMenu.setExtendedState(JFrame.MAXIMIZED_BOTH);
                        mainMenu.setTitle("Point Of Sales");
                        // m.setIconImage(getToolkit().getImage("src/com/resources/icon/icons8-online-store-100.png"));
                        mainMenu.setVisible(true);
                        this.setVisible(false);
                    } else {
                        JOptionPane.showMessageDialog(this, "Nama Anda Dan Password Tidak Cocok ..!",
                                "Error", JOptionPane.ERROR_MESSAGE, icon);
                        jTextField1.setText("");
                        jPasswordField1.setText("");
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Terjadi Error Pada :\n" + ex.toString(),
                            "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }

    private void authUserPrivillege() {
        try {
            if (rs.getString(8).equals("4")) {
                mainMenu.jMenu2.setEnabled(false);
            } else if (!rs.getString(8).equals("1")) {
                mainMenu.jMenu1.setEnabled(false);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Terjadi Error Pada :\n" + ex.toString(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

