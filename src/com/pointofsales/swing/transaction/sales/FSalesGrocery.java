    protected void saveDataPayment() {
        try {
            conn.setAutoCommit(false);
            Time sqlTimes = new Time(td.getTime());
            String sql = "Insert Into Sales Values (?,?,?,?,?,?,?,?,?,?)";
            pst = conn.prepareStatement(sql);
            pst.setString(1, null);
            pst.setString(2, fm.format(td));
            pst.setTime(3, sqlTimes);
            pst.setInt(4, empID);
            pst.setInt(5, custID);
            pst.setInt(6, getItemFreight());
            pst.setInt(7, diskon);
            pst.setInt(8, getSubtotal());
            pst.setInt(9, getMoneyCash());
            pst.setInt(10, getReturnPayment());
            pst.executeUpdate();
            saveDataDetailPayment();
            updateItemStockWhenTransactionSuccess();

            String check = "Select MTotal From Sales Where SaleID=?";
            pst = conn.prepareStatement(check);
            pst.setString(1, Integer.toString(getBillNumber()));
            rs = pst.executeQuery();
            rs.next();
            if (rs.getFloat(1) < 0) {
                conn.rollback();
                JOptionPane.showMessageDialog(null, "Maaf, Transaksi Penjualan Gagal...\nSilakan Coba Lagi...", "Kesalahan", JOptionPane.PLAIN_MESSAGE, getErrorIcon());
            } else {
                conn.commit();
                JOptionPane.showMessageDialog(null, "Selamat, Transaksi Penjualan Berhasil...", "Informasi", JOptionPane.PLAIN_MESSAGE, getOkIcon());
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Terjadi Error Pada:\n" + ex.toString(), "Kesalahan", JOptionPane.PLAIN_MESSAGE, getErrorIcon());
        }
    }

