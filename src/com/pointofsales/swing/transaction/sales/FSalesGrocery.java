    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed
        switch (evt.getKeyCode()) {
            case KeyEvent.VK_F1:
                authPayment();
                break;
            case KeyEvent.VK_F2:
                openForms();
                break;
            case KeyEvent.VK_F5:
                logOut();
                break;
            case KeyEvent.VK_ESCAPE:
                this.dispose();
                break;
            default:
                break;
        }
    }//GEN-LAST:event_formKeyPressed

